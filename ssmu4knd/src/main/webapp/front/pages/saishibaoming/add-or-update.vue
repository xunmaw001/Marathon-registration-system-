<script>
	import wPicker from "@/components/w-picker/w-picker.vue";

	export default {
		data() {
			return {
				ruleForm: {
				bianhao: this.getUUID(),
				bisaimingcheng: '',
				baomingneirong: '',
				baomingshijian: '',
				baomingrenshu: '',
				yonghuming: '',
				xingming: '',
				shouji: '',
				sfsh: '',
				shhf: '',
				userid: '',
				},
				baomingrenshuOptions: [],
				baomingrenshuIndex: 0,
				// 登陆用户信息
				user: {},
                                ro:{
                                   bianhao : false,
                                   bisaimingcheng : false,
                                   baomingneirong : false,
                                   baomingshijian : false,
                                   baomingrenshu : false,
                                   yonghuming : false,
                                   xingming : false,
                                   shouji : false,
                                   sfsh : false,
                                   shhf : false,
                                   userid : false,
                                },
			}
		},
		components: {
			wPicker
		},
		computed: {
			
			
			
			
			
			
			
			
			
			
			
		},
		async onLoad(options) {
    		        this.ruleForm.baomingshijian = this.$utils.getCurDate();
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.yonghuming = this.user.yonghuming
			this.ruleForm.xingming = this.user.xingming
			this.ruleForm.shouji = this.user.shouji

			// 自定义下拉框值
			this.baomingrenshuOptions = "1".split(',')

			// 如果有登陆，获取登陆后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`saishibaoming`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='bianhao'){
					this.ruleForm.bianhao = obj[o];
					this.ro.bianhao = true;
					continue;
					}
					if(o=='bisaimingcheng'){
					this.ruleForm.bisaimingcheng = obj[o];
					this.ro.bisaimingcheng = true;
					continue;
					}
					if(o=='baomingneirong'){
					this.ruleForm.baomingneirong = obj[o];
					this.ro.baomingneirong = true;
					continue;
					}
					if(o=='baomingshijian'){
					this.ruleForm.baomingshijian = obj[o];
					this.ro.baomingshijian = true;
					continue;
					}
					if(o=='baomingrenshu'){
					this.ruleForm.baomingrenshu = obj[o];
					this.ro.baomingrenshu = true;
					continue;
					}
					if(o=='yonghuming'){
					this.ruleForm.yonghuming = obj[o];
					this.ro.yonghuming = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='shouji'){
					this.ruleForm.shouji = obj[o];
					this.ro.shouji = true;
					continue;
					}
					if(o=='userid'){
					this.ruleForm.userid = obj[o];
					this.ro.userid = true;
					continue;
					}
				}
				this.ruleForm.baomingrenshu = 0;
				this.ro.baomingrenshu = false;
			}
			this.styleChange()
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv .cu-form-group .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数

			baomingshijianChange(e) {
				this.ruleForm.baomingshijian = e.target.value;
				this.$forceUpdate();
			},


			// 下拉变化
			baomingrenshuChange(e) {
				this.baomingrenshuIndex = e.target.value
				this.ruleForm.baomingrenshu = this.baomingrenshuOptions[this.baomingrenshuIndex]
			},


			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				var obj = uni.getStorageSync('crossObj');
				var table = uni.getStorageSync('crossTable');
				obj.baomingrenshu = parseFloat(obj.baomingrenshu) + parseFloat(this.ruleForm.baomingrenshu)
				await this.$api.update(`${table}`, obj);
				if((!this.ruleForm.baomingneirong)){
					this.$utils.msg(`报名内容不能为空`);
					return
				}
				if((!this.ruleForm.baomingrenshu)){
					this.$utils.msg(`报名人数不能为空`);
					return
				}
				if(this.ruleForm.baomingrenshu&&(!this.$validate.isIntNumer(this.ruleForm.baomingrenshu))){
					this.$utils.msg(`报名人数应输入整数`);
					return
				}
				if(this.ruleForm.id){
					await this.$api.update(`saishibaoming`, this.ruleForm);
				}else{
					await this.$api.add(`saishibaoming`, this.ruleForm);
				}
				this.$utils.msgBack('提交成功');
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				this.$refs[str].show();
			}
		}
	}
</script>

